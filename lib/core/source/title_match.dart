/// 繁体逐字折成简体(只替换在表内的繁体字)。给「多源同名去重」把繁简变体归到一起。
String foldTradSimp(String s) {
  if (s.isEmpty) return s;
  final buf = StringBuffer();
  for (final ch in s.split('')) {
    buf.write(_t2s[ch] ?? ch);
  }
  return buf.toString();
}

/// 去重键:先折繁→简再归一 → 「绝世武神」「絕世武神」得到同一 key。多源同名去重专用。
String dedupKey(String title) => normalizeTitle(foldTradSimp(title));
